package br.com.byd.veiculo;

public class Veiculo {

	//atributos
	private int ano;
	private String cor;
	private String modelo;
	
	//construtor
	public Veiculo(int ano, String cor, String modelo) {
		super();
		this.ano = ano;
		this.cor = cor;
		this.modelo = modelo;
	}


//metodo
	public void mover() {
		System.out.println("Veiculo movendo...");
	}
	//metodo
	public void parar() {
		System.out.println("Veiculo parando...");


	}
}

